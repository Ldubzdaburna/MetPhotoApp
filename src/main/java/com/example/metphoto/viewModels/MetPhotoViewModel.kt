    private var currentIndex = 0

    private val _artworks = MutableStateFlow<List<ArtworkDetails>>(emptyList())
    val artworks: StateFlow<List<ArtworkDetails>> = _artworks

    init {
        loadMore()
    }

    fun loadMore() {
        viewModelScope.launch {
            var added = 0
            val currentList = _artworks.value.toMutableList()

            while (added < 5 && currentIndex < allObjectIDs.size) {
                val objectId = allObjectIDs[currentIndex]
                currentIndex++

                val details = fetchArtworkDetails(objectId)
                if (details != null) {
                    currentList.add(details)
                    added++
                }
            }

            _artworks.value = currentList
        }
    }

    private suspend fun fetchArtworkDetails(objectId: Int): ArtworkDetails? {
        return withContext(Dispatchers.IO) {
            try {
                val url = URL("https://collectionapi.metmuseum.org/public/collection/v1/objects/$objectId")
                val json = JSONObject(url.readText())

                val imageUrl = json.optString("primaryImage")
                if (imageUrl.isEmpty()) return@withContext null

                val title = json.optString("title", "Unknown Title")
                val year = json.optString("objectDate", "Unknown Year")
                val artist = json.optString("artistDisplayName").ifEmpty {
                    json.optString("culture", "Unknown Origin")
                }

                ArtworkDetails(
                    objectID = objectId,
                    imageUrl = imageUrl,
                    title = title,
                    year = year,
                    artist = artist
                )
            } catch (e: Exception) {
                null
            }
        }
    }
